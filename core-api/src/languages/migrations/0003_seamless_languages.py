def seed(apps, schema_editor):
    Language = apps.get_model("languages", "Language")
    TranslationModel = apps.get_model("translation_models", "TranslationModel")
    ModelLanguage = apps.get_model("translation_models", "ModelLanguage")

    languages = [
        Language(
            code=code,
            api_code=_api_code_for_seamless(code),
            name_en=name,
            script=script,
            supports_source_speech=src_sp,
            supports_source_text=src_tx,
            supports_target_speech=tgt_sp,
            supports_target_text=tgt_tx,
        )
        for code, name, script, src_sp, src_tx, tgt_sp, tgt_tx in SEAMLESS_LANGUAGES
    ]
    Language.objects.bulk_create(languages, ignore_conflicts=True)

    try:
        model = TranslationModel.objects.get(slug="seamless_m4t")
    except TranslationModel.DoesNotExist:
        return

    ModelLanguage.objects.filter(model=model).delete()

    lang_by_code = {
        lang.code: lang
        for lang in Language.objects.filter(code__in={c for c, *_ in SEAMLESS_LANGUAGES})
    }
    sources = [l for l in lang_by_code.values() if l.supports_source_speech or l.supports_source_text]
    targets = [l for l in lang_by_code.values() if l.supports_target_speech or l.supports_target_text]

    seen_pairs: set[tuple[str, str]] = set()
    pairs = []
    for src in sources:
        for tgt in targets:
            if src.api_code == tgt.api_code:
                continue
            key = (src.api_code, tgt.api_code)
            if key in seen_pairs:
                continue
            seen_pairs.add(key)
            pairs.append(
                ModelLanguage(
                    model=model,
                    source_language_code=src.api_code,
                    target_language_code=tgt.api_code,
                    source_model_lang_code=src.code,
                    target_model_lang_code=tgt.code,
                    source_name_en=src.name_en,
                    target_name_en=tgt.name_en,
                    dataset_name="seamless_m4t_v2",
                )
            )

    ModelLanguage.objects.bulk_create(pairs, batch_size=500, ignore_conflicts=True)

    for src_api, tgt_api, chrf in SEAMLESS_FLORES_CHRF:
        ModelLanguage.objects.filter(
            model=model,
            source_language_code=src_api,
            target_language_code=tgt_api,
        ).update(bleu=chrf, dataset_name="flores_devtest_t2tt_chrf2pp")

    for tgt_api, bleu in SEAMLESS_COVOST2_BLEU:
        ModelLanguage.objects.filter(
            model=model,
            source_language_code="en",
            target_language_code=tgt_api,
        ).update(bleu=bleu, dataset_name="covost2_bleu")


